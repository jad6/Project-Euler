 ## About Project Euler

 ### What is Project Euler?
 Project Euler is a series of challenging mathematical/computer programming problems that will require more than just 
 mathematical insights to solve. Although mathematics will help you arrive at elegant and efficient methods, the use of
 a computer and programming skills will be required to solve most problems.

 The motivation for starting Project Euler, and its continuation, is to provide a platform for the inquiring mind to
 delve into unfamiliar areas and learn new concepts in a fun and recreational context.

 ### Who are the problems aimed at?
 The intended audience include students for whom the basic curriculum is not feeding their hunger to learn, adults 
 whose background was not primarily mathematics but had an interest in things mathematical, and professionals who want
 to keep their problem solving and mathematics on the cutting edge.

 ### Can anyone solve the problems?
 The problems range in difficulty and for many the experience is inductive chain learning. That is, by solving one 
 problem it will expose you to a new concept that allows you to undertake a previously inaccessible problem. So the 
 determined participant will slowly but surely work his/her way through every problem.

